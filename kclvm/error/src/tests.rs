use crate::*;

#[test]
fn test_bug_macro() {
    let result = std::panic::catch_unwind(|| {
        bug!();
    });
    assert!(result.is_err());
    let result = std::panic::catch_unwind(|| {
        bug!("an error msg");
    });
    assert!(result.is_err());
    let result = std::panic::catch_unwind(|| {
        bug!("an error msg with string format {}", "msg");
    });
    assert!(result.is_err());
}
